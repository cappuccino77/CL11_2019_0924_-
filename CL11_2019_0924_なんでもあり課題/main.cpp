void BattleScene(void)
{
	//VARIABLE var;

	system("cls");
	crocodile();
	printf("ワニが現れた！\n");
	printf("1:攻撃　2：逃げる＞");

	//数値が正しく入力されているか
	do
	{
		rewind(stdin);
		scanf("%d", &var.ChoicesNumFun);

		if (var.ChoicesNumFun == 1)
		{
			var.JudgmentFun = 1;
		}
		else if (var.ChoicesNumFun == 2)
		{
			var.JudgmentFun = 2;
		}
		else
		{
			printf("数値を正しく入力してね！\n");
			printf("1:攻撃　2：逃げる＞");
		}
	} while (var.JudgmentFun != 1 && var.JudgmentFun != 2);

	if (var.JudgmentFun == 1)				//攻撃を選択した場合
	{
		{
			system("cls");
			crocodile();
			printf("ワニを倒した！\n");
			var.EnemyDestroyingCountFun += 1;
			var.CrocodileDestroyingCountFun += 1;
			var.ChoicesNumFun = 0;
			var.JudgmentFun = 0;
		}
	}
	else if (var.JudgmentFun == 2)					//逃げるを選択した場合
	{
		system("cls");
		printf("ワニから逃げた！\n");
		var.ChoicesNumFun = 0;
		var.JudgmentFun = 0;
	}
	rewind(stdin);
	getchar();
}

